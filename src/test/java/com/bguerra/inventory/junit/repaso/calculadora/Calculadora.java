package com.bguerra.inventory.junit.repaso.calculadora;

public class Calculadora {
    float sumar(float a, float b){
        return a+b;
    }

    int dividir(int a, int b){
        return a/b;
    }

    int multiplicar(int a, int b){
        return a*b;
    }

    int restar(int a,int b){
        return a - b;
    }
}
